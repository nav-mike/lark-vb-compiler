package codegen;

/**
 * Класс, описывающий тип массива.
 * @version 1.0
 */
public class ArrayType {
    
    /** Массив логических значений. */
    public static final byte T_BOOLEAN = 0x4;
    /** Массив символов. */
    public static final byte T_CHAR    = 0x5;
    /** Массив целых чисел. */
    public static final byte T_INT     = 0xA;
    
}
